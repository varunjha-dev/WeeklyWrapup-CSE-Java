public class bitwise {
  public static void main(String[] args) {
    System.out.println(2 & 3);
    System.out.println(2 | 3);
    System.out.println(2 ^ 3);
    System.out.println(~ 2); // similar to 1's complement
    System.out.println(34 << 1); // Generally * 2 (Doubling of given value)
    System.out.println(34 << 2); // Generally * 2 to the power 2 shift
    System.out.println(34 >> 2); // 2 times Shifted Generally / 2 to the power 2 
    System.out.println(-4 << 1); 
    System.out.println(-4 >> 1); // Generally / 2 (Halving of given value)
  }
}
